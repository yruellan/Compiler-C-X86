int main(){

    print(15 + 5); // 20
    print(15 - 5); // 10
    print(15 * 5); // 75
    print(15 / 7); // 2
    print(15 % 7); // 1

    print(-7); // -7
    print(!7); // 0
    print(~7); // -8

    print(11 & 13); // 9
    print(11 | 13); // 15
    print(11 ^ 13); // 4

    print(11 << 1); // 22
    print(11 >> 1); // 5
    
    print(0 ? 10 : 20);  // 10
    print(1 ? 10 : 20);  // 20

    return 0 ;
}